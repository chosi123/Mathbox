def add(a, b):
  return a+b

def subtract(a, b):
  return a-b

def multiply(a, b):
  return a * b

def divide(a, b):
  return a/b

def getMedian(a, b):
    return (a+b)/2

def getRemainder(a, b):
    return a//b

def get_abs(num):
    if num < 0:
        return -num
    return num

def getPercent(a, b):
    return (a/b) * 100
